package Experiment.Personal_Lab;

public class TenToFourtyNine {
    public static void main(String[] args) {

        int number = 10;
        for (int i = 0; i < 4; i++) {
            for (int j = 0; j < 10; j++) {
                System.out.print(number + "\t");
                number++;
            }

            System.out.println();
        }

    }
}
